package com.matheus.training_java;

public class Primitives {

    public static void main(String[] args) {

        String name = "Matheus";
        System.out.println(name);

        boolean brasilian = true;
        System.out.println(brasilian);

        byte smallNumber = 127;
        System.out.println(smallNumber);

        short mediumNumber = 32700;
        System.out.println(mediumNumber);

        int number = 2140000000;
        System.out.println(number);

        long bigNumber = 9100000000000000000L;
        System.out.println(bigNumber);

        double numberDot = 211.200;
        System.out.println(numberDot);

        float numberDotLong = 211.11111111111F;
        System.out.println(numberDotLong);
    }
}
